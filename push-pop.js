var myName = ['Mohammad', 'Abdur', 'Rahman'];
console.log(myName);
// Add Element Using Push in Array
myName.push('Riad');
console.log(myName);
// Remove Element Using Pop in Array
var nickName = myName.pop();
console.log(myName);
// Store Poped Element
console.log(nickName);

// Add Element in First Index
myName.unshift('My Name is');
console.log(myName);

// Remove Element From First Index
myName.shift();
console.log(myName);
